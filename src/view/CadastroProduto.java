    private void btn_finalizarCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_finalizarCadastroActionPerformed
        // TODO add your handling code here:

        if (!(txt_codigo.getText().isEmpty() | txt_nome.getText().isEmpty() | txt_precoDeCusto.getText().isEmpty())) {
            long codigo = Long.parseLong(txt_codigo.getText());
            String nomeProduto = txt_nome.getText();
            String descricao = txt_descricao.getText();
            String getPrecoDeCusto = txt_precoDeCusto.getText();
            double precoDeCusto = Double.parseDouble(getPrecoDeCusto.replace(",", "."));
            String getPreco = txt_preco.getText();
            double preco = Double.parseDouble(getPreco.replace(",", "."));
            int qtdEstoque = 0;
            if (!txt_estoque.getText().isEmpty()) {
                qtdEstoque = Integer.parseInt(txt_estoque.getText());
            }
            Produto produto = new Produto(qtdEstoque, codigo, nomeProduto, descricao, preco, precoDeCusto);
            ProdutoDAO daoProduto = new ProdutoDAO();
            if (daoProduto.inserirNovoProduto(produto)) {
                JOptionPane.showMessageDialog(null, "Produto cadastrado!");
            }

            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Os campos a seguir devem obrigatoriamente ser preenchidos:"
                    + "\n - Código"
                    + "\n - Nome"
                    + "\n - Preço de Custo"
                    + "\n - Preço");
        }
    }//GEN-LAST:event_btn_finalizarCadastroActionPerformed

