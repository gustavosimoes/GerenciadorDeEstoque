    private void txt_lucroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_lucroActionPerformed
        double lucro = 1 + (Double.parseDouble(txt_lucro.getText().replace(",", ".")) / 100);
        double precoDeCusto = Double.parseDouble(txt_precoDeCusto.getText().replace(",", "."));
        double preco = precoDeCusto * lucro;
        txt_preco.setValue(preco);
    }//GEN-LAST:event_txt_lucroActionPerformed

